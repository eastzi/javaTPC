package javaTPC;

public class TPC02 {

	public static void main(String[] args) {
		// 프로그래밍의 3대요소 : 변수, 자료형(데이터 타입), 할당연산자(=, 대입연산자)
		// ex) 1 + 1 = 2
		// int형 a, b, c 변수 선언 
		int a, b, c; 
		a=1;
		b=1;
		c=a+b;
		System.out.println(c);
		
		float f;
		f = 34.5f; 
		System.out.println(f);
		
		char d;
		d = 'A'; //' ' - 싱글따움표로 묶어서 하나만 저장
		System.out.println(d);
		
		boolean g; 
		g = true; 
		System.out.println(g); 

	}

}
